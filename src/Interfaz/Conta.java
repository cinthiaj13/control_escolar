        // TODO add your handling code here:
        Connection con = null;
        con=getConnection();
        DefaultTableModel tconsulta = new DefaultTableModel();
        tconsulta.addColumn("ID_Pago");
        tconsulta.addColumn("Cantidad_pago");
        tconsulta.addColumn("Metodo_Pago");
        tconsulta.addColumn("Fecha_Pago");
        tconsulta.addColumn("Mes");
        tconsulta.addColumn("Matricula");
        tconsulta.addColumn("Grado");
        tconsulta.addColumn("Grupo");
        tconsulta.addColumn("Tipo_Plantel");
        String matricula = MatriculaRI.getText();

        TablaRI.setModel(tconsulta);

        String []datos = new String[9];
        try{
            Statement leer = con.createStatement();
            rs = leer.executeQuery("SELECT tbl_pago.ID_Pago, TBL_PAGO.Cantidad_pago, tbl_pago.Metodo_Pago, TBL_PAGO.Fecha_Pago, tbl_pago.Mes, TBL_Alumno.Matricula, tbl_pago.Grado, tbl_pago.Grupo, TBL_Plantel.Tipo_Plantel FROM tbl_pago  INNER JOIN tbl_alumno ON tbl_pago.matricula = tbl_alumno.matricula  INNER JOIN tbl_plantel ON tbl_plantel.id_plantel = tbl_alumno.id_plantel WHERE tbl_pago.matricula= '"+matricula+"';");
            while(rs.next()){
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                datos[7] = rs.getString(8);
                datos[8] = rs.getString(9);

                tconsulta.addRow(datos);
            }
            TablaRI.setModel(tconsulta);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e + "Error en la consulta");
        }
