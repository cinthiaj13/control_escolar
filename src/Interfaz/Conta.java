    public int print(Graphics graf, PageFormat pagefor, int index) throws PrinterException {
        if(index>0){
                return NO_SUCH_PAGE;
        }

        Graphics2D hub = (Graphics2D) graf;
        hub.translate(pagefor.getImageableX() + 30,pagefor.getImageableY() + 30);
        hub.scale(0.6, 0.6);
        
        int ri = ContaTab.getSelectedIndex();
        if(ri == 3){
            TablaRI.printAll(graf);
        }
        else if(ri == 4){
            ReporteGralTabla.printAll(graf);
        }
       
        return PAGE_EXISTS;
    }
    
    
    
    
